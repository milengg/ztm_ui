<script>
    const blindsContainer = document.getElementById('blinds-main');
    const blindsRange = document.getElementById('blinds');

    const blinds = [
      { value: 30, svg: 'blinds-1', class: 'text-teal-custom' },
      { value: 60, svg: 'blinds-2', class: 'text-yellow-custom' },
      { value: 80, svg: 'blinds-3', class: 'text-red-main' },
    ];

    function updateBlinds() {
      const value = parseInt(blindsRange.value);
    
      const blind = blinds.reduce((prev, curr) => {
        return curr.value <= value ? curr : prev;
      }, blinds[0]);

      const newClass = blind.class;
      const oldClass = blindsContainer.getAttribute('class');

      if(blind.svg === 'blinds-1')
      {
        document.getElementById('blinds-3').style.display = 'none';
        document.getElementById('blinds-2').style.display = 'none';
        document.getElementById('blinds-1').style.display = 'block';
      } else if(blind.svg === 'blinds-2') {
        document.getElementById('blinds-1').style.display = 'none';
        document.getElementById('blinds-3').style.display = 'none';
        document.getElementById('blinds-2').style.display = 'block';
      } else if(blind.svg === 'blinds-3') {
        document.getElementById('blinds-1').style.display = 'none';
        document.getElementById('blinds-2').style.display = 'none';
        document.getElementById('blinds-3').style.display = 'block';
      }

      if(oldClass) {
          blindsContainer.classList.remove(oldClass);
        }
        blindsContainer.classList.add(newClass);
      }

    blindsRange.addEventListener('input', updateBlinds);
 
    updateBlinds();
</script>
